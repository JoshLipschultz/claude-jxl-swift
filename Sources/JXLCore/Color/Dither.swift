/// The dither offset for pixel (x, y) of output channel `c` — scalar
/// equivalent of libjxl `MakeUnsigned`'s table load (lane 0; the padded
/// columns make the SIMD form identical to `% 32` indexing).
@inline(__always)
func blueNoiseDither(_ x: Int, _ y: Int, _ c: Int) -> Float {
    let xOff = (x + c * 23) % 32
    let yOff = (y + c * 13) % 32
    return kBlueNoiseDither[yOff * 48 + xOff]
}

/// Quantizes an encoded sample (nominal [0, 1]) to 8 bits with blue-noise
/// dithering: `v*255 + dither`, clamp to [0, 255], round to nearest even —
/// the exact operation order of libjxl `MakeUnsigned<uint8_t>`.
@inline(__always)
func ditherQuantize8(_ encoded: Float, _ x: Int, _ y: Int, _ c: Int) -> UInt8 {
    var v = encoded * 255
    v += blueNoiseDither(x, y, c)
    v = max(0, min(255, v))
    return UInt8(v.rounded(.toNearestOrEven))
}
